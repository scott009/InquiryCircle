def main():
    """Update JSON with Japanese translations for Chapter 9."""
    input_file = Path("/mnt/c/Users/scott/Documents/AIProjects/Markdown/ThaiTranslation/rdg_en_v3.json")

    print(f"Loading {input_file}...")
    with open(input_file, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Create backup
    backup_file = input_file.with_name('rdg_en_v3.json.backup_before_japanese')
    print(f"Creating backup at {backup_file}...")
    with open(backup_file, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print("Adding Japanese translations to Chapter 9...")

    # Find and update Chapter 9
    updated_count = 0
    for section in data['content']:
        if 'chapters' in section:
            for chapter in section['chapters']:
                if chapter.get('title') == 'WHAT IS RECOVERY DHARMA?':
                    # Add Japanese title
                    chapter['japanese_title'] = JAPANESE_TITLE
                    print(f"✓ Updated chapter title")

                    # Update paragraphs
                    for sec in chapter.get('sections', []):
                        for content_item in sec.get('content', []):
                            if content_item.get('type') == 'paragraph':
                                para_id = content_item['id']
                                if para_id in JAPANESE_PARAGRAPHS:
                                    content_item['japanese_text'] = JAPANESE_PARAGRAPHS[para_id]
                                    updated_count += 1
                                    print(f"✓ Updated {para_id}")

    print(f"\nSaving updated JSON...")
    with open(input_file, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"\n✅ Japanese translations added successfully!")
    print(f"   Updated: 1 title + {updated_count} paragraphs")
    print(f"   Backup saved at: {backup_file}")

if __name__ == "__main__":
    main()
